class player():

    def __init__(self):
        self.points = None
        self.turnovers = None
        self.assists = None
        self.rebounds = None
        self.blocks = None
        self.steals = None

    def getPoints(self):
        return self.points

    def getTurnovers(self):
        return self.turnovers

    def getAssists(self):
        return self.assists

    def getRebounds(self):
        return self.rebounds

    def getBlocks(self):
        return self.blocks

    def getSteals(self):
        return self.steals

    
